public class Main
{
    public static void main(String[] args)
    {
        int a = 6;
        int b = a<<1; // Left-Shift Operator by 1 will Multiply by 2  (value of b must be 12)
        int c = a>>1; // Right-Shift Operator by 1 will Divide by 2 (value of c must be 3)
        System.out.println("Value of b is : " + b);
        System.out.println("Value of c is : " + c);
    }
}